#include <avr/io.h>
#include <stdint.h>

#include "lgt8f328p_spec.h"
#include "lgt8f328p_conf.h"

/**********************************************************************************
*** 						  	EXPORTED FUNCTIONS								*** 													
**********************************************************************************/

// ----------------------------------------------------------------------
// public: AC0 module initialization
// ----------------------------------------------------------------------
void ac0Init(void)
{
	ac0SetPosedge(AC0_PSEL);
	ac0SetNegedge(AC0_NSEL);

	C0SR |= (1 << AC0_C0IE) | AC0_ISS;
	C0XR |= (1 << AC0_C0OE) | (1 << C0HYSE);	
}

// ----------------------------------------------------------------------
// public: AC1 module initialization
// ----------------------------------------------------------------------
void ac1Init(void)
{
	ac1SetPosedge(AC1_PSEL);
	ac1SetNegedge(AC1_NSEL);

	C1SR |= (1 << AC1_C1IE) | AC1_ISS;
	C1XR |= (1 << AC1_C1OE) | (1 << C1HYSE);	
}



